"""
Smart Parking System v2.0 — Database Layer
SQLite database with 14 tables supporting multi-location parking,
user accounts, bookings, payments, AI recommendations, and chatbot history.

Tables:
  1.  parking_locations   — Registry of parking venues
  2.  parking_zones       — Subdivisions within a location
  3.  parking_config      — Per-zone sensor/capacity configuration
  4.  parking_status      — Live per-zone occupancy (updated every event)
  5.  occupancy_log       — Immutable event stream (every ENTRY/EXIT)
  6.  predictions         — ML predictions per zone
  7.  daily_summary       — Aggregated daily stats per zone
  8.  users               — User accounts
  9.  sessions            — Login sessions (token-based auth)
  10. bookings            — Parking reservations
  11. payments            — Payment records
  12. pricing_rules       — Per-location pricing tiers
  13. ai_recommendations  — Smart messages generated by AI per zone
  14. chatbot_history     — User ↔ AI conversation log
"""

