_SCHEMA_SQL = """

-- ─── TABLE 1: Parking Locations ───
-- Each row is a physical venue (mall, airport, office, university, hospital).
CREATE TABLE IF NOT EXISTS parking_locations (
    location_id     TEXT PRIMARY KEY,
    name            TEXT NOT NULL,
    address         TEXT NOT NULL,
    city            TEXT NOT NULL,
    latitude        REAL NOT NULL,
    longitude       REAL NOT NULL,
    location_type   TEXT NOT NULL CHECK(location_type IN (
        'MALL', 'AIRPORT', 'CORPORATE', 'UNIVERSITY', 'HOSPITAL'
    )),
    operating_hours TEXT NOT NULL DEFAULT '00:00-23:59',
    image_url       TEXT,
    created_at      TEXT NOT NULL
);

-- ─── TABLE 2: Parking Zones ───
-- Subdivisions within a location (e.g., "Ground Floor", "Basement P1").
-- Each zone has its own capacity and sensor array.
CREATE TABLE IF NOT EXISTS parking_zones (
    zone_id         TEXT PRIMARY KEY,
    location_id     TEXT NOT NULL,
    zone_name       TEXT NOT NULL,
    max_capacity    INTEGER NOT NULL CHECK(max_capacity > 0),
    floor_level     TEXT,
    is_active       INTEGER NOT NULL DEFAULT 1,
    created_at      TEXT NOT NULL,
    FOREIGN KEY (location_id) REFERENCES parking_locations(location_id)
);

-- ─── TABLE 3: Parking Config ───
-- Per-zone technical configuration (sensor count, thresholds).
CREATE TABLE IF NOT EXISTS parking_config (
    zone_id         TEXT PRIMARY KEY,
    sensor_count    INTEGER NOT NULL DEFAULT 2,
    warning_threshold REAL NOT NULL DEFAULT 0.8,
    critical_threshold REAL NOT NULL DEFAULT 0.95,
    FOREIGN KEY (zone_id) REFERENCES parking_zones(zone_id)
);

-- ─── TABLE 4: Parking Status ───
-- Live occupancy per zone. Updated on every ENTRY/EXIT event.
CREATE TABLE IF NOT EXISTS parking_status (
    zone_id             TEXT PRIMARY KEY,
    current_count       INTEGER NOT NULL DEFAULT 0,
    max_capacity        INTEGER NOT NULL,
    available_slots     INTEGER NOT NULL,
    utilization_percent REAL NOT NULL DEFAULT 0.0,
    is_full             INTEGER NOT NULL DEFAULT 0,
    last_event_type     TEXT,
    last_updated        TEXT NOT NULL,
    FOREIGN KEY (zone_id) REFERENCES parking_zones(zone_id)
);

-- ─── TABLE 5: Occupancy Log ───
-- Immutable append-only event stream. Every ENTRY/EXIT ever recorded.
CREATE TABLE IF NOT EXISTS occupancy_log (
    event_id        INTEGER PRIMARY KEY AUTOINCREMENT,
    zone_id         TEXT NOT NULL,
    event_type      TEXT NOT NULL CHECK(event_type IN ('ENTRY', 'EXIT')),
    event_time      TEXT NOT NULL,
    occupancy_after INTEGER NOT NULL CHECK(occupancy_after >= 0),
    FOREIGN KEY (zone_id) REFERENCES parking_zones(zone_id)
);

-- ─── TABLE 6: Predictions ───
-- ML predictions per zone (predicted occupancy, peak hours, utilization).
CREATE TABLE IF NOT EXISTS predictions (
    prediction_id   INTEGER PRIMARY KEY AUTOINCREMENT,
    zone_id         TEXT NOT NULL,
    predicted_at    TEXT NOT NULL,
    predicted_count INTEGER NOT NULL,
    peak_hour_start TEXT,
    peak_hour_end   TEXT,
    utilization_avg REAL,
    model_type      TEXT DEFAULT 'linear_regression',
    mae             REAL,
    FOREIGN KEY (zone_id) REFERENCES parking_zones(zone_id)
);

-- ─── TABLE 7: Daily Summary ───
-- Aggregated daily statistics per zone.
CREATE TABLE IF NOT EXISTS daily_summary (
    date            TEXT NOT NULL,
    zone_id         TEXT NOT NULL,
    total_entries   INTEGER NOT NULL DEFAULT 0,
    total_exits     INTEGER NOT NULL DEFAULT 0,
    peak_count      INTEGER NOT NULL DEFAULT 0,
    avg_utilization REAL NOT NULL DEFAULT 0.0,
    PRIMARY KEY (date, zone_id),
    FOREIGN KEY (zone_id) REFERENCES parking_zones(zone_id)
);

-- ─── TABLE 8: Users ───
-- User accounts for the parking system.
CREATE TABLE IF NOT EXISTS users (
    user_id         INTEGER PRIMARY KEY AUTOINCREMENT,
    name            TEXT NOT NULL,
    email           TEXT NOT NULL UNIQUE,
    phone           TEXT,
    password_hash   TEXT NOT NULL,
    vehicle_plate   TEXT,
    created_at      TEXT NOT NULL,
    updated_at      TEXT NOT NULL
);

-- ─── TABLE 9: Sessions ───
-- Login sessions with token-based authentication.
CREATE TABLE IF NOT EXISTS sessions (
    session_id      INTEGER PRIMARY KEY AUTOINCREMENT,
    user_id         INTEGER NOT NULL,
    token           TEXT NOT NULL UNIQUE,
    created_at      TEXT NOT NULL,
    expires_at      TEXT NOT NULL,
    is_active       INTEGER NOT NULL DEFAULT 1,
    FOREIGN KEY (user_id) REFERENCES users(user_id)
);

-- ─── TABLE 10: Bookings ───
-- Parking reservations by users.
CREATE TABLE IF NOT EXISTS bookings (
    booking_id      INTEGER PRIMARY KEY AUTOINCREMENT,
    user_id         INTEGER NOT NULL,
    zone_id         TEXT NOT NULL,
    booking_time    TEXT NOT NULL,
    start_time      TEXT NOT NULL,
    end_time        TEXT,
    status          TEXT NOT NULL DEFAULT 'CONFIRMED' CHECK(status IN (
        'CONFIRMED', 'ACTIVE', 'COMPLETED', 'CANCELLED'
    )),
    vehicle_plate   TEXT,
    FOREIGN KEY (user_id) REFERENCES users(user_id),
    FOREIGN KEY (zone_id) REFERENCES parking_zones(zone_id)
);

-- ─── TABLE 11: Payments ───
-- Payment records linked to bookings.
CREATE TABLE IF NOT EXISTS payments (
    payment_id      INTEGER PRIMARY KEY AUTOINCREMENT,
    booking_id      INTEGER NOT NULL,
    user_id         INTEGER NOT NULL,
    amount          REAL NOT NULL CHECK(amount >= 0),
    currency        TEXT NOT NULL DEFAULT 'INR',
    method          TEXT NOT NULL DEFAULT 'SIMULATED' CHECK(method IN (
        'SIMULATED', 'UPI', 'CARD', 'WALLET'
    )),
    status          TEXT NOT NULL DEFAULT 'PENDING' CHECK(status IN (
        'PENDING', 'PAID', 'REFUNDED', 'FAILED'
    )),
    transaction_id  TEXT,
    created_at      TEXT NOT NULL,
    FOREIGN KEY (booking_id) REFERENCES bookings(booking_id),
    FOREIGN KEY (user_id) REFERENCES users(user_id)
);

-- ─── TABLE 12: Pricing Rules ───
-- Per-location pricing tiers.
CREATE TABLE IF NOT EXISTS pricing_rules (
    pricing_id      INTEGER PRIMARY KEY AUTOINCREMENT,
    location_id     TEXT NOT NULL,
    rate_per_hour   REAL NOT NULL CHECK(rate_per_hour >= 0),
    rate_per_day    REAL NOT NULL CHECK(rate_per_day >= 0),
    currency        TEXT NOT NULL DEFAULT 'INR',
    surge_multiplier REAL NOT NULL DEFAULT 1.0,
    is_active       INTEGER NOT NULL DEFAULT 1,
    FOREIGN KEY (location_id) REFERENCES parking_locations(location_id)
);

-- ─── TABLE 13: AI Recommendations ───
-- Smart messages generated by the AI engine per zone.
CREATE TABLE IF NOT EXISTS ai_recommendations (
    rec_id          INTEGER PRIMARY KEY AUTOINCREMENT,
    zone_id         TEXT NOT NULL,
    message         TEXT NOT NULL,
    category        TEXT NOT NULL CHECK(category IN (
        'CALM', 'INFORMATIONAL', 'ALERT', 'URGENT', 'CRITICAL', 'FULL_REDIRECT'
    )),
    utilization_at  REAL NOT NULL,
    alternative_zone TEXT,
    created_at      TEXT NOT NULL,
    FOREIGN KEY (zone_id) REFERENCES parking_zones(zone_id)
);

-- ─── TABLE 14: Chatbot History ───
-- User ↔ AI conversation log.
CREATE TABLE IF NOT EXISTS chatbot_history (
    chat_id         INTEGER PRIMARY KEY AUTOINCREMENT,
    user_id         INTEGER,
    user_query      TEXT NOT NULL,
    bot_response    TEXT NOT NULL,
    intent          TEXT,
    created_at      TEXT NOT NULL,
    FOREIGN KEY (user_id) REFERENCES users(user_id)
);

-- ─── INDEXES for query performance ───
CREATE INDEX IF NOT EXISTS idx_occupancy_zone_time
    ON occupancy_log(zone_id, event_time);
CREATE INDEX IF NOT EXISTS idx_daily_zone_date
    ON daily_summary(zone_id, date);
CREATE INDEX IF NOT EXISTS idx_bookings_user
    ON bookings(user_id, status);
CREATE INDEX IF NOT EXISTS idx_payments_user
    ON payments(user_id);
CREATE INDEX IF NOT EXISTS idx_sessions_token
    ON sessions(token, is_active);
CREATE INDEX IF NOT EXISTS idx_zones_location
    ON parking_zones(location_id);
"""


