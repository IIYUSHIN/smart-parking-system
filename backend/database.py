def log_event(db_path: str, zone_id: str,
              event_type: str, occupancy_after: int,
              event_time: str | None = None) -> int:
    """Appends to occupancy_log. Returns event_id."""
    if event_type not in ("ENTRY", "EXIT"):
        raise ValueError(f"Invalid event_type: {event_type}")
    if event_time is None:
        event_time = _now_iso()

    conn = get_connection(db_path)
    try:
        cursor = conn.execute(
            "INSERT INTO occupancy_log "
            "(zone_id, event_type, event_time, occupancy_after) "
            "VALUES (?, ?, ?, ?)",
            (zone_id, event_type, event_time, occupancy_after)
        )
        conn.commit()
        return cursor.lastrowid
    finally:
        conn.close()


def batch_log_events(db_path: str, events: list[tuple]) -> int:
    """Bulk inserts events into occupancy_log in a single transaction.
    Each event is a tuple: (zone_id, event_type, event_time, occupancy_after).
    Returns total events inserted."""
    if not events:
        return 0
    conn = get_connection(db_path)
    try:
        conn.executemany(
            "INSERT INTO occupancy_log "
            "(zone_id, event_type, event_time, occupancy_after) "
            "VALUES (?, ?, ?, ?)",
            events
        )
        conn.commit()
        return len(events)
    finally:
        conn.close()


def get_history(db_path: str, zone_id: str,
                hours: int = 24) -> list[dict]:
    """Returns occupancy_log events from last N hours for a zone."""
    cutoff = (datetime.now(timezone.utc) - timedelta(hours=hours)).isoformat(
        timespec='seconds')

    conn = get_connection(db_path)
    try:
        rows = conn.execute(
            "SELECT event_id, event_type, event_time, occupancy_after "
            "FROM occupancy_log "
            "WHERE zone_id = ? AND event_time >= ? "
            "ORDER BY event_time DESC",
            (zone_id, cutoff)
        ).fetchall()
        return [dict(r) for r in rows]
    finally:
        conn.close()


def get_all_events(db_path: str, zone_id: str) -> list[dict]:
    """Returns ALL occupancy_log events for a zone, ordered by time."""
    conn = get_connection(db_path)
    try:
        rows = conn.execute(
            "SELECT event_id, event_type, event_time, occupancy_after "
            "FROM occupancy_log "
            "WHERE zone_id = ? "
            "ORDER BY event_time ASC",
            (zone_id,)
        ).fetchall()
        return [dict(r) for r in rows]
    finally:
        conn.close()


def get_location_events(db_path: str, location_id: str) -> list[dict]:
    """Returns ALL events for ALL zones in a location, ordered by time."""
    conn = get_connection(db_path)
    try:
        rows = conn.execute("""
            SELECT ol.event_id, ol.zone_id, ol.event_type,
                   ol.event_time, ol.occupancy_after, pz.zone_name
            FROM occupancy_log ol
            JOIN parking_zones pz ON ol.zone_id = pz.zone_id
            WHERE pz.location_id = ?
            ORDER BY ol.event_time ASC
        """, (location_id,)).fetchall()
        return [dict(r) for r in rows]
    finally:
        conn.close()


