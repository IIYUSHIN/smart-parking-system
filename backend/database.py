def init_db(db_path: str, locations: list | None = None) -> None:
    """Creates all 14 tables and seeds location/zone/pricing data
    from the LOCATIONS config if not already present."""
    conn = get_connection(db_path)
    try:
        conn.executescript(_SCHEMA_SQL)

        if locations:
            now = _now_iso()
            for loc in locations:
                # Check if location already exists
                existing = conn.execute(
                    "SELECT 1 FROM parking_locations WHERE location_id = ?",
                    (loc["location_id"],)
                ).fetchone()
                if existing:
                    continue

                # Insert location
                conn.execute(
                    "INSERT INTO parking_locations "
                    "(location_id, name, address, city, latitude, longitude, "
                    "location_type, operating_hours, image_url, created_at) "
                    "VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)",
                    (loc["location_id"], loc["name"], loc["address"],
                     loc["city"], loc["latitude"], loc["longitude"],
                     loc["location_type"], loc["operating_hours"],
                     loc.get("image_url"), now)
                )

                # Insert zones and their status rows
                for zone in loc["zones"]:
                    conn.execute(
                        "INSERT INTO parking_zones "
                        "(zone_id, location_id, zone_name, max_capacity, created_at) "
                        "VALUES (?, ?, ?, ?, ?)",
                        (zone["zone_id"], loc["location_id"],
                         zone["zone_name"], zone["max_capacity"], now)
                    )
                    # Initialize parking_config with defaults
                    conn.execute(
                        "INSERT INTO parking_config "
                        "(zone_id, sensor_count, warning_threshold, critical_threshold) "
                        "VALUES (?, 2, 0.8, 0.95)",
                        (zone["zone_id"],)
                    )
                    # Initialize parking_status at 0 occupancy
                    conn.execute(
                        "INSERT INTO parking_status "
                        "(zone_id, current_count, max_capacity, available_slots, "
                        "utilization_percent, is_full, last_updated) "
                        "VALUES (?, 0, ?, ?, 0.0, 0, ?)",
                        (zone["zone_id"], zone["max_capacity"],
                         zone["max_capacity"], now)
                    )

                # Insert pricing rules
                pricing = loc.get("pricing", {})
                conn.execute(
                    "INSERT INTO pricing_rules "
                    "(location_id, rate_per_hour, rate_per_day, currency) "
                    "VALUES (?, ?, ?, ?)",
                    (loc["location_id"],
                     pricing.get("rate_per_hour", 0),
                     pricing.get("rate_per_day", 0),
                     pricing.get("currency", "INR"))
                )

            conn.commit()
    finally:
        conn.close()


