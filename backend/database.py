def aggregate_daily(db_path: str, zone_id: str, date: str,
                    max_capacity: int) -> dict:
    """Computes and stores daily_summary for a zone on a given date."""
    conn = get_connection(db_path)
    try:
        entries = conn.execute(
            "SELECT COUNT(*) as cnt FROM occupancy_log "
            "WHERE zone_id = ? AND event_type = 'ENTRY' "
            "AND event_time LIKE ?",
            (zone_id, f"{date}%")
        ).fetchone()["cnt"]

        exits = conn.execute(
            "SELECT COUNT(*) as cnt FROM occupancy_log "
            "WHERE zone_id = ? AND event_type = 'EXIT' "
            "AND event_time LIKE ?",
            (zone_id, f"{date}%")
        ).fetchone()["cnt"]

        peak_row = conn.execute(
            "SELECT MAX(occupancy_after) as peak FROM occupancy_log "
            "WHERE zone_id = ? AND event_time LIKE ?",
            (zone_id, f"{date}%")
        ).fetchone()
        peak_count = peak_row["peak"] if peak_row["peak"] is not None else 0

        avg_row = conn.execute(
            "SELECT AVG(occupancy_after) as avg_occ FROM occupancy_log "
            "WHERE zone_id = ? AND event_time LIKE ?",
            (zone_id, f"{date}%")
        ).fetchone()
        avg_occ = avg_row["avg_occ"] if avg_row["avg_occ"] is not None else 0
        avg_util = (avg_occ / max_capacity * 100) if max_capacity > 0 else 0.0

        conn.execute(
            "INSERT INTO daily_summary "
            "(date, zone_id, total_entries, total_exits, "
            "peak_count, avg_utilization) "
            "VALUES (?, ?, ?, ?, ?, ?) "
            "ON CONFLICT(date, zone_id) DO UPDATE SET "
            "total_entries = excluded.total_entries, "
            "total_exits = excluded.total_exits, "
            "peak_count = excluded.peak_count, "
            "avg_utilization = excluded.avg_utilization",
            (date, zone_id, entries, exits, peak_count, round(avg_util, 1))
        )
        conn.commit()

        return {
            "date": date, "zone_id": zone_id,
            "total_entries": entries, "total_exits": exits,
            "peak_count": peak_count, "avg_utilization": round(avg_util, 1)
        }
    finally:
        conn.close()


