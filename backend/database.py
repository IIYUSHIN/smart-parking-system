def _hash_password(password: str) -> str:
    """Creates a SHA-256 hash of the password with a salt prefix."""
    salt = "sp_v2_salt_"
    return hashlib.sha256((salt + password).encode('utf-8')).hexdigest()


def create_user(db_path: str, name: str, email: str,
                password: str, phone: str = None,
                vehicle_plate: str = None) -> dict:
    """Creates a new user account. Returns user dict.
    Raises ValueError if email already exists."""
    now = _now_iso()
    pw_hash = _hash_password(password)

    conn = get_connection(db_path)
    try:
        existing = conn.execute(
            "SELECT 1 FROM users WHERE email = ?", (email,)
        ).fetchone()
        if existing:
            raise ValueError(f"Email already registered: {email}")

        cursor = conn.execute(
            "INSERT INTO users "
            "(name, email, phone, password_hash, vehicle_plate, "
            "created_at, updated_at) "
            "VALUES (?, ?, ?, ?, ?, ?, ?)",
            (name, email, phone, pw_hash, vehicle_plate, now, now)
        )
        conn.commit()
        return {
            "user_id": cursor.lastrowid,
            "name": name, "email": email,
            "phone": phone, "vehicle_plate": vehicle_plate,
            "created_at": now
        }
    finally:
        conn.close()


