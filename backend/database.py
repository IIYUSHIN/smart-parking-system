def calculate_parking_fee(db_path: str, location_id: str,
                          hours: float) -> dict:
    """Calculates the parking fee for given duration at a location."""
    conn = get_connection(db_path)
    try:
        pricing = conn.execute(
            "SELECT rate_per_hour, rate_per_day, currency, surge_multiplier "
            "FROM pricing_rules WHERE location_id = ? AND is_active = 1",
            (location_id,)
        ).fetchone()
        if not pricing:
            return {"amount": 0, "currency": "INR", "breakdown": "No pricing set"}

        rate_hr = pricing["rate_per_hour"]
        rate_day = pricing["rate_per_day"]
        surge = pricing["surge_multiplier"]

        # Use daily rate if cheaper for stays > X hours
        hourly_cost = hours * rate_hr * surge
        daily_cost = rate_day * surge
        if hours >= 8 and daily_cost < hourly_cost:
            amount = daily_cost
            breakdown = f"Daily rate: {rate_day} x {surge} surge = {amount}"
        else:
            amount = hourly_cost
            breakdown = f"{hours}h x {rate_hr}/hr x {surge} surge = {amount}"

        return {
            "amount": round(amount, 2),
            "currency": pricing["currency"],
            "breakdown": breakdown,
            "surge_multiplier": surge
        }
    finally:
        conn.close()


